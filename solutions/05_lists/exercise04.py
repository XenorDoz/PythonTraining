# Given this tuple

coords = (12, 5, 9, 3, 5, 12)

# Display the first value and the last value

print(coords[0])
print(coords[-1])

# Display the length of the tuple using `len()`

print(len(coords))

# Display all values except the first two (use slicing)

print(coords[2:])

# Display how many times the value `12` appears in a tuple (with `<tuple name>.count()`)

print(coords.count(12))